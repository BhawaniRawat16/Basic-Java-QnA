class PreDefineConstantMethod {
    public static void main(String[] args) {
        /// Constants
        System.out.println(Math.PI);
        System.out.println(Math.E);

        /// Methods
        System.out.println(Math.abs(-6)); // 6
        System.out.println(Math.max(34, 45)); // 45
        System.out.println(Math.min(34, 45)); // 34

        System.out.println(Math.ceil(10.3)); // 11
        System.out.println(Math.floor(10.3)); // 10

        System.out.println(Math.sqrt(9)); // 3
        System.out.println(Math.sqrt(20)); // .......

        System.out.println(Math.pow(2, 3)); // 8

        System.out.println(Math.random() * 100); // 0 to 1

        System.out.println(Math.round(9.3)); // 9
        System.out.println(Math.round(9.7)); // 10
    }
}
